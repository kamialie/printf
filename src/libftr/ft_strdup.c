#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*res;
	int		i;

	if ((res = (char *)malloc(sizeof(char) * (ft_strlen(src) + 1))) == NULL)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		res[i] = src[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
