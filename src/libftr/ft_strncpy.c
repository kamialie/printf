#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char *point;

	point = dst;
	while (len > 0 && *src != '\0')
	{
		*dst = *src;
		dst++;
		src++;
		len--;
	}
	while (len > 0)
	{
		*dst = '\0';
		dst++;
		len--;
	}
	return (point);
}
